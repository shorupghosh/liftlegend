export function parseCSVLine(line: string): string[] {
  const result: string[] = [];
  let current = '';
  let inQuotes = false;
  for (let i = 0; i < line.length; i++) {
    const char = line[i];
    if (char === '"') {
      if (inQuotes && line[i + 1] === '"') {
        current += '"';
        i++;
      } else {
        inQuotes = !inQuotes;
      }
    } else if (char === ',' && !inQuotes) {
      result.push(current);
      current = '';
    } else {
      current += char;
    }
  }
  result.push(current);
  return result;
}

export function generateCSVContent(headers: string[], rows: (string | number)[][]): string {
  const formatCell = (cell: string | number) => {
    if (typeof cell === 'string') {
      return `"${cell.replace(/"/g, '""')}"`;
    }
    return cell;
  };

  const csvRows = rows.map((row) => row.map(formatCell).join(','));
  return [headers.join(','), ...csvRows].join('\n');
}

export function triggerCSVDownload(content: string, filename: string) {
  const blob = new Blob([content], { type: 'text/csv;charset=utf-8;' });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
}
